	/**
	 * Script
	 *
	 * Generates link to a JS file
	 *
	 * @param	mixed	stylesheet src or an array
	 * @param	string	type
	 * @param	string	text
	 * @param	bool	async
	 * @param	bool	defer
	 * @param	string	crossorigin
	 * @param	bool	should index_page be added to the css path
	 * @return	string
	 */
	function script_tag($src='', $type='', $text='', $async=FALSE, $defer=FALSE, $crossorigin='', $index_page=FALSE) {
		$CI =& get_instance();
		//$link = '<link ';
		$script = array( "<script" );
		
		if (is_array($src)) {
			foreach ($src as $k => $v) {
				if ($k === 'src' && ! preg_match('#^([a-z]+:)?//#i', $v)) {
					if ($index_page === TRUE) $script[] = 'src="'.$CI->config->site_url($src).'"';
					else $script[] = 'src="'.$CI->config->slash_item('base_url').$src.'"';
				}
				elseif (preg_match('/async|defer/', $k) && !empty($v)) $script[] = $k;
				elseif ($k === 'type' && (!empty($v) && $v = get_script_type($v))) $script[] = 'type="'.$v.'"';
				elseif ($k === 'crossorigin' && !empty($v)) $script[] = get_script_crossorigin($v);
				else $script[] = "$k=\"$v\"";
			}
		}
		else {
			if ($async) $script[] = 'async';
			
			if (preg_match('#^([a-z]+:)?//#i', $src)) $script[] = 'src="'.$src.'"';
			elseif ($index_page === TRUE) $script[] = 'src="'.$CI->config->site_url($src).'"';
			else $script[] = 'src="'.$CI->config->slash_item('base_url').$src.'"';
			
			if (!empty($type) && $type = get_script_type($type)) $script[] = 'type="'.$type.'"';
			
			if ($defer) $script[] = 'defer';
			
			if (!empty($crossorigin)) $script[] = get_script_crossorigin($crossorigin);
		}
		$script[] = ">$text</script>";
		return implode(" ", $script);
	}
	
	function get_script_crossorigin($val) {
		switch ($val) {
			case 1:
			case 'credentials':
			case 'use-credentials':
				return 'crossorigin="use-credentials"';
			default:
				return 'crossorigin="anonymous"';
		}
		return '';
	}
	
